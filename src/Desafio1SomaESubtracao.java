//Desafio 1 soma e subtracao de variaveis
public class Desafio1SomaESubtracao {
    public static void main(String[] args){
        int primeiroNumero = 1987;
        int segundoNumero = 568;
        int somaDosNumeros = primeiroNumero+ segundoNumero;
        int subtracaoDosNumeros = primeiroNumero- segundoNumero;
        System.out.printf("A soma de "+primeiroNumero+" e "+ segundoNumero +" é "+ somaDosNumeros +"\n");
        System.out.printf("A subtração de "+primeiroNumero+" e "+ segundoNumero +" é "+ subtracaoDosNumeros +"\n");
    }
}